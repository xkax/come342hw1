
public class Company {
    
    private String cname;
    private String cid;
    private double ckar;

    public Company() {
       
    }

  
    public String getCname() {
        return cname;
    }

   
    public void setCname(String cname) {
        this.cname = cname;
    }

    
    public String getCid() {
        return cid;
    }

   
    public void setCid(String cid) {
        this.cid = cid;
    }

   
    public double getCkar() {
        return ckar;
    }

   
    public void setCkar(double ckar) {
        this.ckar = ckar;
    }
    
    
}
